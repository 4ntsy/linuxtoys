#!/bin/bash
# NAME: Backup Documents
# VERSION: 1.0
# DESCRIPTION: Creates a compressed backup of your 'Documents' folder on your Desktop.
# ICON: folder-zip

# --- Start of the script code ---
echo "Preparing to back up your Documents folder..."
echo "Calculating size..."
sleep 2
echo "Compressing files... (This could take a while)"
sleep 4
echo "Backup complete! 'Documents_Backup.tar.gz' has been saved to your Desktop."
